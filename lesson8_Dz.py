def отримати_дані_користувача():
    while True:
        try:
            імя = input("Введіть ім'я: ").strip()
            if not імя:
                print("[!] Помилка! Ім'я не може бути пустим")
                continue
            
            try:
                вік = int(input("Введіть вік: "))
                
                if вік < 1 or вік > 120:
                    print("[!] Помилка! Вік має бути від 1 до 120")
                    continue
                
                print(f"[OK] Привіт {імя}! Тобі {вік} років")
                print()
                return імя, вік
            
            except ValueError:
                print("[!] Помилка! Вік має бути числом")
        
        except Exception as e:
            print(f"[!] Помилка: {e}")

імя, вік = отримати_дані_користувача()



def працювати_з_масивом():
    числа = [10, 25, 35, 42, 55]
    print(f"Список: {числа}")
    
    while True:
        try:
            try:
                індекс = int(input("Введіть індекс (0-4): "))
            except ValueError:
                print("[!] Помилка! Індекс має бути числом")
                continue
            
            елемент = числа[індекс]
            print(f"[OK] Елемент за індексом {індекс}: {елемент}")
            print()
            break
        
        except IndexError:
            print(f"[!] Помилка! Індекс має бути від 0 до {len(числа)-1}")

працювати_з_масивом()



def читати_файл_безпечно():
    імя_файлу = input("Введіть назву файлу: ")
    
    try:
        with open(імя_файлу, 'r', encoding='utf-8') as f:
            вміст = f.read()
            print("[OK] Вміст файлу:")
            print(вміст)
            print()
    
    except FileNotFoundError:
        print(f"[!] Помилка! Файл '{імя_файлу}' не знайдено")
    except Exception as e:
        print(f"[!] Помилка: {e}")

читати_файл_безпечно()


def конвертер_валют():
    курси = {
        'USD': 1.0,
        'EUR': 0.92,
        'GBP': 0.79,
        'JPY': 149.5,
        'UAH': 41.5
    }
    
    try:
        print("Доступні: USD, EUR, GBP, JPY, UAH")
        
        try:
            сума = float(input("Введіть суму: "))
        except ValueError:
            print("[!] Помилка! Сума має бути числом")
            return
        
        if сума < 0:
            print("[!] Помилка! Сума не може бути від'ємною")
            return
        
        із_валюти = input("З (USD, EUR тощо): ").upper()
        в_валюту = input("У (USD, EUR тощо): ").upper()
        
        if із_валюти not in курси:
            print(f"[!] Помилка! Валюта '{із_валюти}' не знайдена")
            return
        
        if в_валюту not in курси:
            print(f"[!] Помилка! Валюта '{в_валюту}' не знайдена")
            return
        
        сума_usd = сума / курси[із_валюти]
        результат = сума_usd * курси[в_валюту]
        
        print(f"[OK] {сума} {із_валюти} = {результат:.2f} {в_валюту}")
        print()
    
    except Exception as e:
        print(f"[!] Помилка: {e}")

конвертер_валют()



def оцінки_студента():
    try:
        try:
            кількість = int(input("Скільки оцінок? "))
            if кількість <= 0:
                print("[!] Помилка! Має бути більше 0")
                return
        except ValueError:
            print("[!] Помилка! Має бути числом")
            return
        
        оцінки = []
        print("Введіть оцінки від 1 до 100:")
        
        try:
            for i in range(кількість):
                try:
                    оцінка = float(input(f"Оцінка {i+1}: "))
                    if оцінка < 0 or оцінка > 100:
                        print("[!] Помилка! Оцінка має бути від 0 до 100")
                        continue
                    оцінки.append(оцінка)
                except ValueError:
                    print("[!] Помилка! Оцінка має бути числом")
        except Exception as e:
            print(f"[!] Помилка: {e}")
        
        if not оцінки:
            print("[!] Помилка! Оцінок не введено")
            return
        
        try:
            середнє = sum(оцінки) / len(оцінки)
            
            if середнє >= 90:
                буква = "відмінно"
            elif середнє >= 80:
                буква = "добре"
            elif середнє >= 70:
                буква = "задовільно"
            elif середнє >= 60:
                буква = "незадовільно"
            else:
                буква = "провал"
            
            print(f"[OK] Оцінки: {оцінки}")
            print(f"[OK] Середнє: {середнє:.2f}")
            print(f"[OK] Оцінка: {буква}")
            print()
        
        except ZeroDivisionError:
            print("[!] Помилка! Ділення на нуль неможливе")
    
    except Exception as e:
        print(f"[!] Помилка: {e}")

оцінки_студента()
